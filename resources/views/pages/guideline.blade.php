<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><b>SDAI
</b>= Simplified Disease Activity Index</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><a href="http://www.rheumatology.org/Portals/0/Files/SDAI%20Form.pdf">http://www.rheumatology.org/Portals/0/Files/SDAI%20Form.pdf</a></p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><br/>

</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	É
o índice simplificado de atividade da doença (artrite reumatoide).
Não necessita de calculadora (ao contrário do DAS).</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	-
Como calcular: é a soma linear dos seguintes parâmetros:</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	28
articulações dolorosas (cada uma pontua 1 ponto)</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	28
articulações edemaciadas (cada uma pontua 1 ponto)</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	EVA
doença – paciente (0-10)</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	EVA
doença – médico (0-10)</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	PCR
(em mg/dL) – de zero a 10.</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	</p>
<p align="justify" style="text-indent: 1.25cm; margin-bottom: 0cm; line-height: 100%">
Total máximo: 86 pontos. Como interpretar:</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&lt;=
3,3: remissão</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&gt;
3,3 a &lt;=11: atividade baixa</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&gt;
11 a &lt;=26: atividade moderada</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&gt;26:
atividade alta</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><br/>

</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	Melhora
pequena: &gt;=50%</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	M.
moderada: &gt;=70%</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	M.
importante: &gt;=85%</p>
<p style="margin-bottom: 0cm; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0cm; line-height: 100%"><br/>

</p>
