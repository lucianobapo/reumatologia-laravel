<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	</p>
<p align="justify" style="text-indent: 1.25cm; margin-bottom: 0cm; line-height: 100%">
Índice de atividade de Bath para a espondilite anquilosante</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	Escore
de zero a 10. Se &gt;=4: indica atividade da doença.</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	Uma
mudança maior ou igual a 2 pontos após intervenção terapêutica é
considerada significativa.</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	Como
calcular: são 6 perguntas. Obter a média das perguntas 5 e 6
(gravidade e duração da rigidez matinal). Esta média é somada ao
resultado das perguntas 1-4 e obtém-se uma média de 5 valores, o
que resulta em um valor de 0 a 10.</p>
