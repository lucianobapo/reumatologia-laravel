<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><b>DAS
28</b> = Disease Activity Score. Calculador:</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><a href="http://www.das-score.nl/das28/en/">http://www.das-score.nl/das28/en/</a></p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">DAS28
– Artigo original</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><a href="http://www.rheumatology.org/Portals/0/Files/Modified%20Disease%20Activity%20Score_28.pdf">http://www.rheumatology.org/Portals/0/Files/Modified%20Disease%20Activity%20Score_28.pdf</a></p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	</p>
<p align="justify" style="text-indent: 1.25cm; margin-bottom: 0cm; line-height: 100%">
Escore de atividade da doença (artrite reumatoide). Avalia 28
articulações (IFPs, MCFs, punhos, cotovelos, ombros e joelhos), VHS
ou PCR, e percepção global da doença pelo paciente (EVA de 0-100).
</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	No
site oficial (link acima), há também versões sem a escala visual
analógica (EVA) e a versão original com 44 articulações (DAS44 ou
simplesmente DAS). 
</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	-
Interpretação:</p>
<p align="justify" style="text-indent: 1.25cm; margin-bottom: 0cm; line-height: 100%">
&lt;2,6: remissão</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	2,6
a 3,2: atividade baixa</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&gt;3,2
a 5,1: atividade moderada</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	&gt;5,1:
atividade alta</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><br/>

</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%">	Diminuição
de 0,6: resposta moderada. 
</p>
<p align="justify" style="text-indent: 1.25cm; margin-bottom: 0cm; line-height: 100%">
Diminuição de 1,2: resposta importante.  
</p>
<p align="justify" style="margin-bottom: 0cm; line-height: 100%"><br/>

